import Foundation
import UIKit

extension UIFont {
    
    static func poppinsRegular(size: CGFloat) -> UIFont {
        UIFont(name: "Poppins-Regular", size: size)!
    }
    
    static func poppinsMedium(size: CGFloat) -> UIFont {
        UIFont(name: "Poppins-Medium", size: size)!
    }
}
